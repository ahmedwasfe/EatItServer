package ahmet.com.eatitserver.model.Food;

public class FoodSize {

    private String name;
    private Long price;

    public FoodSize() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPrice() {
        return price;
    }

    public void setPrice(Long price) {
        this.price = price;
    }
}
